plt.plot(n_sizes, sa_accuracy, marker='o', color='#1f77b4', linewidth=2, label='SA Accuracy')
plt.plot(n_sizes, ga_accuracy, marker='^', color='#ff7f0e', linewidth=2, label='GA Accuracy')

plt.ylabel('Solution Success Rate (%)', fontweight='bold')
